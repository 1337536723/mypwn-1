from z3 import *

aa=ord('%')
bb=ord('a')
cc=ord('\n')
a=Int('a')
b=Int('b')
c=Int('c')
d=Int('d')
s=Solver()
s.add(a>=0 , a < 64)
s.add(b>=0 , b < 64)
s.add(c>=0 , c < 64)
s.add(d>=0 , d < 64)
s.add(aa==(4*a+(b-b%16)/16)%256)
s.add((16*b+ (c-c%4)/4)%256==bb)
s.add(cc==(d+64*c)%256)
print(s.check())
print(s.model())